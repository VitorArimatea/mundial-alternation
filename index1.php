        <h1>Copa do mundo</h1>
        <h2>História</h2>
            <p>O Campeonato Mundial de Futebol, mais conhecido no Brasil por Copa do Mundo e também conhecida em 
                Portugal como Campeonato do Mundo de Futebol, Campeonato Mundial de Futebol, Campeonato Mundial 
                FIFA, ou simplesmente Mundial, é uma competição internacional de futebol que ocorre a cada quatro anos, 
                com exceção de 1942 e 1946 quando não foi realizada por conta da Segunda Guerra Mundial. Essa competição,
                que foi criada em 1928 na França, sob a liderança do presidente Jules Rimet, está aberta a todas as federações 
                reconhecidas pela FIFA (Federação Internacional de Futebol Associado, em francês: Fédération International de 
                Football Association). O antigo nome da taça faz referência a Jules Rimet. A primeira edição ocorreu em 1930 
                no Uruguai, escolhida como país sede cuja seleção saiu vencedora.</p>
        <h2>Antes da Segunda Guerra</h2>
            <p>Devido ao sucesso dos torneios olímpicos de futebol, a FIFA, liderada pelo seu presidente Jules Rimet, 
                novamente começou a organizar seu próprio torneio internacional fora das Olimpíadas. Em 28 de maio 
                de 1928, o Congresso da FIFA em Amsterdã decidiu realizar um campeonato mundial. Por ser o então 
                bicampeão olímpico de futebol e comemorando o centenário de sua independência em 1930, a FIFA nomeou 
                o Uruguai como país-sede da primeira Copa do Mundo.</p>
        <h2>Após a Segunda Guerra</h2>
            <p>A Copa do Mundo FIFA de 1950, realizada no Brasil, foi a primeira com participação da Inglaterra, sendo 
                que esta seleção se retirou da FIFA em 1920 pela recusa de jogar contra os países que estiveram em guerra 
                e em protesto contra a influência estrangeira no futebol, retornando em 1946 após convite da FIFA. O Uruguai 
                também retornou após não participar das duas edições anteriores, sagrando-se campeão novamente do 
                torneio após vencer o Brasil por 2-1 na final, em partida que ficou conhecida como "Maracanaço".</p>
        <h2>Expansão para 32 equipes</h2>
            <p>O torneio foi expandido para 24 equipes em 1982 e para 32 equipes em 1998, permitindo que mais seleções
                da África, Ásia e América do Norte participassem. Desde então, as seleções destas regiões tiveram mais sucesso, 
                com várias avançando nas fases finais da competição, sendo o México nas quartas de final em 1986, Camarões
                nas quartas de final em 1990, Coreia do Sul conquistando o quarto lugar em 2002; Senegal e Estados Unidos 
                nas quartas de final em 2002, Gana nas quartas de final em 2010 e Costa Rica nas quartas de final em 2014. 
                Contudo, as equipes europeias e sul-americanas possuem um nível técnico bastante superior, sendo, por 
                exemplo, os únicos a disputar as quartas de final nas edições de 1994, 1998, 2006 e 2018, assim como equipes
                destes continentes foram as únicas a disputar as finais do torneio.</p>
        <h2>Expansão para 48 equipes</h2>
            <p>Em outubro de 2013, o presidente da FIFA Joseph Blatter informou que estava sendo estudada a ideia de 
                garantir vagas separadas para a União Caribenha de Futebol no torneio. Na edição de 25 de outubro de 2013
                do periódico FIFA Weekly. Em outubro de 2016, o presidente da FIFA Gianni Infantino, declarou seu apoio a 
                uma Copa do Mundo de 48 equipes em 2026. Em 10 de janeiro de 2017, a FIFA confirmou que a Copa do 
                Mundo FIFA de 2026 será disputada com 48 equipes, porém ainda não anunciou oficialmente como será feita
                a divisão destas vagas.</p>
        <h2>Outros torneios da FIFA</h2>
            <p>Um torneio equivalente para o futebol feminino, a Copa do Mundo de Futebol Feminino, foi realizada pela
                primeira vez em 1991 na China. O torneio feminino é menor em escala e perfil do que o masculino, porém o
                número de participantes nas eliminatórias para o torneio de 2019 foi de 144, mais que o dobro em 
                comparação com o torneio de 1991.</p>
        <h2>Países</h2>
        <p>Dois continentes historicamente dominam a Copa do Mundo: América do Sul e Europa. Eles venceram todas 
            as edições desde sua criação em 1930. Os oito vencedores diferentes que entraram no ranking da Copa do 
            Mundo são sul-americanos ou europeus. Além disso, entre a conquista do Brasil em 1962 e a da Itália em 2006, 
            as seleções europeias e da América do Sul alternaram o posto de campeão a cada quatro anos, ou seja, nesse 
            período nunca tivemos um continente ganhando duas vezes seguidas o torneio. A organização do evento 
            começou a ocorrer em outros continentes, principalmente na América do Norte (1970, 1986 e 1994), em seguida 
            na Ásia (2002) e finalmente na África (2010). A Copa se globalizou e a competição agora é realmente um evento 
            universal, já que foi sediada em todos os continentes do planeta, com exceção da Oceania.</p>
